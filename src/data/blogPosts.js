// Helper function to get related posts
export const getRelatedPosts = (currentPostId, category, limit = 3) => {
  // First try to find posts in the same category
  let related = blogPosts
    .filter(post => post.id !== currentPostId && post.categories.includes(category))
    .slice(0, limit);
  
  // If we don't have enough related posts, add some recent posts
  if (related.length < limit) {
    const morePostsNeeded = limit - related.length;
    const otherPosts = blogPosts
      .filter(post => post.id !== currentPostId && !related.some(r => r.id === post.id))
      .slice(0, morePostsNeeded);
    
    related = [...related, ...otherPosts];
  }
  
  return related;
};

